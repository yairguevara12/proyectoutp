package segundaparteproy;


public class RegistroPelicula {
    
    String nombre;
    int conteo;

    public RegistroPelicula(String nombre, int conteo) {
        this.nombre = nombre;
        this.conteo = conteo;
    }

    public RegistroPelicula() {
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getConteo() {
        return conteo;
    }

    public void setConteo(int conteo) {
        this.conteo = conteo;
    }

   
    

    
     
      
}
